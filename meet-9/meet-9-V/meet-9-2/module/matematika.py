# Penjumlahan, pengurangan, perkalian, pembagian

def penjumlahan(angka1, angka2):
    hasil = angka1 + angka2
    return hasil


def pengurangan(angka1, angka2):
    hasil = angka1 - angka2
    return hasil


def perkalian(angka1, angka2):
    hasil = angka1 * angka2
    return hasil


def pembagian(angka1, angka2):
    hasil = angka1 / angka2
    return hasil