# Tuple & Set
# List -> []
list_angka = [2,4,6,8]
print(list_angka)
list_angka[2] = 16
print(list_angka)

# Tuple -> () | no operation | tau index
tuple_angka = (2,"4",6,8)
print(tuple_angka)
tuple_angka[2] = 16
print(tuple_angka)

# Set -> {} -> gatau urutannya / no index
set_angka = {1,2,3,4,5}
print(set_angka[0])